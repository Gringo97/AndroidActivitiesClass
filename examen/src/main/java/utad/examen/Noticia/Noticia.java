public class Noticia {
    public String titulo;
    public String descripcion;
    public String urlFoto;

    public Noticia(){}

    public Noticia(String titulo, String descripcion, String urlFoto) {
        this.titulo = titulo;
        this.descripcion = descripcion;
        this.urlFoto = urlFoto;
    }
}
